#include <stdio.h>

int main() {
    int num = 10; // Initialize the starting number

    // Use a while loop to Repetition from 10 to 20
    while (num <= 20) {
      
        printf("%d\n", num); // Display the  number
      
        num++; // Increment the number for the next Repetition
      
    }

    return 0;
}
